package com.example.diucity.Adapter.HomeAdapter;

public class Home_Places {

    int image;
    String title, description;

    public Home_Places(int image, String title, String description) {
        this.image = image;
        this.title = title;
        this.description = description;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }


}
